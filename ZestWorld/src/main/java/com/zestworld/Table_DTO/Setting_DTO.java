public class Setting_DTO {
	private String userid;
	private int assign_f;
	private int end_f;
	private int alarm_f;
	
	
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public int getAssign_f() {
		return assign_f;
	}
	public void setAssign_f(int assign_f) {
		this.assign_f = assign_f;
	}
	public int getEnd_f() {
		return end_f;
	}
	public void setEnd_f(int end_f) {
		this.end_f = end_f;
	}
	public int getAlarm_f() {
		return alarm_f;
	}
	public void setAlarm_f(int alarm_f) {
		this.alarm_f = alarm_f;
	}
	
	
}
